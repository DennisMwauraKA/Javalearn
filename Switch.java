public class Switch {
    public static void main(String[] args) {
        int n = 31;
        switch (n) {
            case 1:
                System.out.println("monday");
                break;
            case 2:
                System.out.println("Tuesday");
                break;
            case 3:
                System.out.println("Wednesday");
                break;
            default:
            System.out.println("enter a valid number");
                break;
        }
    }
}
