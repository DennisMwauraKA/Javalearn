public class LogicalOperators {
    public static void main(String[] args) {
        int x = 7;
        int y = 5;
        int a = 5;
        int b = 9;

        boolean result = x > y || a > b;
        System.out.println(result);
    }
}

// LogicalOperators

// in this we have 3 operators
// &&,||,!

// &&- if any of the variables is false then the whole outcome is false
// ||- if any of the variables is true then the outcome is true